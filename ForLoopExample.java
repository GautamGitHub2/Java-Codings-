package CoreJavaHandsOn;

public class ForLoopExample {

    public static void main(String[] args) {

        for (int i = 1; i <= 10; i++) {
            System.out.println(i);

        }

        System.out.println("2nd For Loop Example");

        for (int j = 10; j > 0; j--) {
            System.out.println(j);
        }

    }

}
